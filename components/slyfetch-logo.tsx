export function SlyFetchLogo({ logoIndex = 0, animate = true, className }: SlyFetchLogoProps) {
  const [isLoaded, setIsLoaded] = useState(!animate)

  useEffect(() => {
    if (animate) {
      const timer = setTimeout(() => {
        setIsLoaded(true)
      }, 300)

      return () => clearTimeout(timer)
    }
  }, [animate])

  // Use the provided logoIndex, ensuring it's within bounds
  const displayLogoIndex = Math.min(Math.max(0, logoIndex), asciiLogos.length - 1)

  return (
    <div className="flex items-center justify-start">
      <pre
        className={cn(
          "font-mono whitespace-pre leading-tight text-[10px] bg-gradient-to-br from-cyan-400 via-purple-400 to-pink-500 text-transparent bg-clip-text",
          isLoaded ? "opacity-100" : "opacity-0",
          "transition-opacity duration-500",
          "ml-0 pl-0", // Ensure no left margin or padding
          className
        )}
      >
        {asciiLogos[displayLogoIndex]}
      </pre>
    </div>
  )
}

